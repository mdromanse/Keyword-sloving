package model;

public class KeywordData {

	  private String keyword;
	    private String longestOption;
	    private String shortestOption;

	    public String getKeyword() {
	        return keyword;
	    }

	    public void setKeyword(String keyword) {
	        this.keyword = keyword;
	    }
	    
	    public String getLongestOption() {
	        return longestOption;
	    }

	    public void setLongestOption(String longestOption) {
	        this.longestOption = longestOption;
	    }

	    public String getShortestOption() {
	        return shortestOption;
	    }

	    public void setShortestOption(String shortestOption) {
	        this.shortestOption = shortestOption;
	    }
	
}
